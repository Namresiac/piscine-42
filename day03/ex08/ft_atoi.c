#include <stdio.h>

int		ft_atoi(char *str)
{
	int	i;
	int n;
	int	v;

	i = 0;
	n = 0;
	v = 0;
	while (((str[i] >= 8) && (str[i] <= 13)) || (str[i] == ' '))
	{
		i++;
	}
	if (str[i] == '-')
		n = -1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while (((str[i] >= '0') && (str[i] <= '9')))
	{
		v = v * 10 + (str[i] - '0');
		i++;
	}
	if (n == -1)
		return (-v);
	else
		return (v);
}

int		main()
{
	printf("%d", ft_atoi("    +735 1 "));
}
