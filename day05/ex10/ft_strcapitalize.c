char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			str[i] = (str[i] - 32);
			i++;
		}
		if ((str[i] == ' ') || (str[i] == '-') || (str[i] == '+'))
			str[i + 1] = (str[i + 1] + 32);
		i++;
	}
	return (str);
}
